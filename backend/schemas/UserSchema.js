const mongoose = require('mongoose');

// The Schema: The "Blueprint" of your data
const UserSchema = new mongoose.Schema({
    firstName: { 
        type: String, 
        required: true, 
        trim: true 
    },
    lastName: { 
        type: String, 
        required: true, 
        trim: true 
    },
    city: String,
    state: String,
    zip: String,
    email: { 
        type: String, 
        required: true, 
        unique: true, 
        lowercase: true 
    },
    password: { 
        type: String, 
        required: true 
    },
    createdAt: { 
        type: Date, 
        default: Date.now 
    }
});

const UserModel = mongoose.model("user", UserSchema);
module.exports = UserSchema;